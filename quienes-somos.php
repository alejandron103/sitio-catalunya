	                <h3>Misión</h3>

	                <p>La Corporación Universitaria de Cataluña es una institución privada de educación virtual que desarrolla las funciones de docencia, investigación y proyección social, de tal manera que transforma vidas por medio de experiencias educativas de la mejor calidad y de forma incluyente, con el propósito de formar ciudadanos íntegros, responsables y con el máximo respeto por las diferencias dentro de la sociedad.</p>

	                <p>La Corporación Universitaria de Cataluña promueve el desarrollo personal, el crecimiento intelectual, la visión global, la tolerancia, la inclusión y la integración. Capacitando al estudiante para actuar en entornos integrados, globales, sostenibles y aplicar conocimiento como medio para enriquecer su vida, la comunidad donde actúa y a la sociedad que integra.</p>

	                <p>En su tarea promueve la unión de los esfuerzos de instituciones de diversos países para ofrecer la mejor formación posible sin limitaciones de fronteras o posibilidades económicas.</p>

